def test_pdf_report_html_includes_weekly_delta_when_enabled(
    authenticated_client, db_session, org_id, seed_org, monkeypatch
):
    monkeypatch.setattr(
        call_import_evaluation_pdf_report_service,
        "_render_weasyprint",
        lambda _html: None,
    )
    call_import, evaluation = _seed_completed_evaluation(db_session, org_id)
    metric = (
        db_session.query(Metric)
        .filter(
            Metric.organization_id == org_id,
            Metric.name == "Escalation Handling",
        )
        .one()
    )
    prior_row = CallImportRow(
        call_import_id=call_import.id,
        organization_id=org_id,
        row_index=2,
        conversation_id="call-prior",
        recording_date=date(2026, 5, 11),
        raw_columns={"conversation_id": "call-prior"},
        status=CallImportRowStatus.COMPLETED,
    )
    db_session.add(prior_row)
    db_session.flush()
    db_session.add(
        CallImportEvaluationRow(
            evaluation_id=evaluation.id,
            call_import_row_id=prior_row.id,
            status="completed",
            metric_scores={
                str(metric.id): {
                    "value": False,
                    "type": "boolean",
                    "rationale": "Escalation was handled.",
                }
            },
        )
    )
    evaluation.total_rows = 2
    evaluation.completed_rows = 2
    call_import.total_rows = 2
    call_import.completed_rows = 2
    db_session.commit()

    rows = (
        db_session.query(CallImportEvaluationRow, CallImportRow)
        .filter(CallImportEvaluationRow.evaluation_id == evaluation.id)
        .join(CallImportRow, CallImportRow.id == CallImportEvaluationRow.call_import_row_id)
        .all()
    )

    pdf_bytes = call_import_evaluation_pdf_report_service.render_pdf(
        vendor_name="Acme Vendor",
        call_import=call_import,
        evaluation=evaluation,
        metrics=[metric],
        rows=rows,
        generated_at=datetime(2026, 5, 28, tzinfo=timezone.utc),
        include_weekly_delta=True,
    )

    assert b"Weekly delta" in pdf_bytes
    assert b"+100.0 pp" in pdf_bytes
    assert b"latest recording-date week" in pdf_bytes


def test_pdf_report_html_separates_business_metrics(
    authenticated_client, db_session, org_id, seed_org
):
    call_import, evaluation = _seed_completed_evaluation(db_session, org_id)
    workspace_id = _default_workspace_id(db_session, org_id)
    metric = Metric(
        organization_id=org_id,
        workspace_id=workspace_id,
        name="Call Context Distribution",
        description="Classifies the business context of each call.",
        metric_type=MetricType.BOOLEAN.value,
        supported_surfaces=["call_imports"],
        enabled_surfaces=["call_imports"],
        capture_rationale=True,
        enabled=True,
    )
    db_session.add(metric)
    db_session.flush()
    eval_row = evaluation.row_results[0]
    evaluation.selected_metric_ids = [*evaluation.selected_metric_ids, str(metric.id)]
    scores = dict(eval_row.metric_scores or {})
    scores[str(metric.id)] = {
        "value": True,
        "type": "boolean",
        "metric_name": metric.name,
        "rationale": "User called about delivery status.",
    }
    eval_row.metric_scores = scores
    db_session.commit()

    rows = (
        db_session.query(CallImportEvaluationRow, CallImportRow)
        .filter(CallImportEvaluationRow.evaluation_id == evaluation.id)
        .join(CallImportRow, CallImportRow.id == CallImportEvaluationRow.call_import_row_id)
        .all()
    )
    summaries = call_import_evaluation_pdf_report_service._summarize_metrics(
        [metric], rows
    )
    html = call_import_evaluation_pdf_report_service._render_html(
        {
            "title": "Quality Metric Audit",
            "subtitle": "Call Import Evaluation Report",
            "vendor_name": "Acme Vendor",
            "generated_at": "May 28, 2026 00:00 UTC",
            "generated_at_iso": "2026-05-28 00:00 UTC",
            "logo_data_uris": [],
            "custom_heading": None,
            "call_import": call_import,
            "evaluation": evaluation,
            "metrics": summaries,
            "rows": rows,
            "internal": False,
            "completion_rate": "100.0%",
            "include_weekly_delta": False,
            "weekly_delta_meta": None,
        }
    )

    assert "03 Business Insights" in html
    assert "Call Context Distribution" in html
    assert "User called about delivery status." in html


def test_pdf_report_uses_saved_report_branding_logo(
    authenticated_client, db_session, org_id, seed_org, monkeypatch
):
    s3_module = import_module("app.services.storage.s3_service")

    fake_s3 = SimpleNamespace(
        download_file_by_key=lambda key: b"custom-logo",
    )
    monkeypatch.setattr(s3_module, "s3_service", fake_s3)
    captured: dict[str, object] = {}

    def fake_render_pdf(**kwargs):
        captured["logo_data_uris"] = kwargs.get("logo_data_uris")
        captured["custom_heading"] = kwargs.get("custom_heading")
        return b"%PDF-1.4\ncustom logo pdf"

    monkeypatch.setattr(
        call_import_evaluation_pdf_report_service,
        "render_pdf",
        fake_render_pdf,
    )
    call_import, evaluation = _seed_completed_evaluation(db_session, org_id)
    workspace = (
        db_session.query(Workspace)
        .filter(Workspace.id == call_import.workspace_id)
        .one()
    )
    workspace.report_branding = {
        "heading": "Acme Custom Heading",
        "images": [
            {
                "id": "img-1",
                "s3_key": "workspace/logo.png",
                "content_type": "image/png",
                "filename": "logo.png",
                "size_bytes": len(b"custom-logo"),
            }
        ],
    }
    db_session.commit()

    response = authenticated_client.post(
        f"/api/v1/call-imports/{call_import.id}/evaluations/{evaluation.id}/pdf-report",
        json={"vendor_name": "Acme Vendor", "report_type": "external"},
    )

    assert response.status_code == 200, response.text
    assert captured["logo_data_uris"] == ["data:image/png;base64,Y3VzdG9tLWxvZ28="]
    assert captured["custom_heading"] == "Acme Custom Heading"
